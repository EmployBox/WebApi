package model;

import util.Streamable;

import java.util.stream.Stream;

public class Account extends AutoGeneratedIdentity<Long> {
    protected long accountID;
    protected final String email;
    protected final String password;
    protected final double rating;
       
    protected final Streamable<Job> offeredJobs;
    protected final Streamable<Comment> comments;
    protected final Streamable<User> following;
    protected final Streamable<Chat> chats;
    protected final Streamable<Rating> ratings;

    protected Account(
            long accountID,
            String email,
            String password,
            double rating,
            long version,
            Streamable<Job> offeredJobs,
            Streamable<Comment> comments,
            Streamable<Chat> chats,
            Streamable<Rating> ratings,
            Streamable<User> following
    ){
        super(accountID, version);
        this.accountID = accountID;
        this.email = email;
        this.password = password;
        this.rating = rating;
        this.offeredJobs = offeredJobs;
        this.chats = chats;
        this.ratings = ratings;
        this.comments = comments;
        this.following = following;
    }

    /**
     * Since we're creating programmatically this object, we don't know the primaryKey value, so we set it to defaultKey
     * @param email
     * @param password
     * @param rating
     * @return
     */
    public static Account create(
            String email,
            String password,
            double rating,
            Streamable<Job> offeredJobs,
            Streamable<Comment> comments,
            Streamable<Chat> chats,
            Streamable<Rating> ratings,
            Streamable<User> followers
    ){

        Account account = new Account(defaultKey, email, password, rating, 0, offeredJobs,comments,chats,ratings, followers);
        account.markNew();
        return account;
    }

    public static Account load(
            long accountID,
            String email,
            String password,
            double rating,
            long version,
            Streamable<Job> offeredJobs,
            Streamable<Comment> comments,
            Streamable<Chat> chats,
            Streamable<Rating> ratings,
            Streamable<User> followers
    ){
        Account account = new Account(accountID, email, password, rating, version, offeredJobs,comments,chats,ratings, followers);
        account.markClean();
        return account;
    }

    public static Account update(
            Account acc,
            String email,
            String password,
            double rating,
            Streamable<Job> offeredJobs,
            Streamable<Comment> comments,
            Streamable<Chat> chats,
            Streamable<Rating> ratings,
            Streamable<User> followers
    ){
        acc.markToBeDirty();
        Account account = new Account(acc.getIdentityKey(), email, password, rating, acc.getNextVersion(), offeredJobs, comments, chats, ratings, followers);
        account.markDirty();
        return account;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public double getRating() {
        return rating;
    }

    public Stream<Job> getOfferedJobs() {
        return offeredJobs.get();
    }

    public Stream<User> getFollowing() {
        return following.get();
    }

    public Stream<Comment> getComments() {
        return comments.get();
    }

    public Stream<Chat> getChats() {
        return chats.get();
    }

    public Stream<Rating> getRatings() {
        return ratings.get();
    }

    @Override
    protected void newdentityKey(Long key) {
        identityKey = key;
        accountID = key;
    }
}
